var x=document.getElementById("demo");
function getLocation(){
    if (navigator.geolocation){
        navigator.geolocation.getCurrentPosition(showPosition,showError);
    }
    else{
        x.innerHTML="Geolocation is not supported by this browser.";
    }
}

function showPosition(position){
    lat=position.coords.latitude;
    lon=position.coords.longitude;
    displayLocation(lat,lon);
}

function showError(error){
    switch(error.code){
        case error.PERMISSION_DENIED:
            alert("User denied the request for Geolocation. Please refresh the page.")
        break;
        case error.POSITION_UNAVAILABLE:
            alert("Location information is unavailable. Please refresh the page.")
        break;
        case error.TIMEOUT:
            alert("The request to get user location timed out. Please refresh the page.")
        break;
        case error.UNKNOWN_ERROR:
            alert("An unknown error occurred. Please refresh the page.")
        break;
    }
}

